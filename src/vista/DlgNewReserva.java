    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Reserva reservaNueva = getReserva();

        if (reservaNueva != null) {
            if (operacion == 1) {
                listaReservas.add(reservaNueva);
                JOptionPane.showMessageDialog(this, "Reserva guardada");
            } else {
                listaReservas.set(index, reservaNueva);
                JOptionPane.showMessageDialog(this, "Reserva editada");
            }

            this.dispose();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

