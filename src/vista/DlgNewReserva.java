    private void iniciarFormulario() {
        cargarClientes();
        cargarVehiculos();
        txtNumReserva.setEditable(false);

        KeyAdapter calcular = new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent evt) {
                calcularTotales();
            }
        };

        txtCantDias.addKeyListener(calcular);
        txtPrecioDia.addKeyListener(calcular);

        if (operacion == 1) {
            txtNumReserva.setText(String.valueOf(generarNumeroReserva()));
        }
    }

    private void calcularTotales() {
        try {
            int cantDias = Integer.parseInt(txtCantDias.getText());
            double precioDia = Double.parseDouble(txtPrecioDia.getText());

            double subtotal = cantDias * precioDia;
            double impuesto = subtotal * 0.13;
            double total = subtotal + impuesto;

            txtSubtotal.setText(String.valueOf(subtotal));
            txtImpuesto.setText(String.valueOf(impuesto));
            txtTotal.setText(String.valueOf(total));
        } catch (NumberFormatException e) {
            txtSubtotal.setText("");
            txtImpuesto.setText("");
            txtTotal.setText("");
        }
    }

    private void cargarClientes() {
        cmbCliente.removeAllItems();

        for (Cliente cliente : listaClientes) {
            cmbCliente.addItem(cliente.getNombre());
        }
    }

    private void cargarVehiculos() {
        cmbVehiculo.removeAllItems();

        for (Vehiculo vehiculo : listaVehiculos) {
            cmbVehiculo.addItem(vehiculo.getPlaca()
                    + " - "
                    + vehiculo.getMarca()
                    + " "
                    + vehiculo.getModelo());
        }
    }

    private int generarNumeroReserva() {
        if (listaReservas.isEmpty()) {
            return 1;
        }

        return listaReservas.get(
                listaReservas.size() - 1
        ).getNumReserva() + 1;
    }

    private Reserva getReserva() {
        Reserva reservaNueva = new Reserva();

        if (dtpFechaReserva.getDate() != null
                && cmbCliente.getSelectedIndex() != -1
                && cmbVehiculo.getSelectedIndex() != -1
                && !txtCantDias.getText().isEmpty()
                && !txtPrecioDia.getText().isEmpty()) {

            try {
                reservaNueva.setNumReserva(
                        Integer.parseInt(txtNumReserva.getText()));

                reservaNueva.setFechaReserva(
                        dtpFechaReserva.getDate());

                Cliente cliente = listaClientes.get(
                        cmbCliente.getSelectedIndex());

                Vehiculo vehiculo = listaVehiculos.get(
                        cmbVehiculo.getSelectedIndex());

                int cantDias = Integer.parseInt(
                        txtCantDias.getText());

                double precioDia = Double.parseDouble(
                        txtPrecioDia.getText());

                if (cantDias <= 0) {
                    JOptionPane.showMessageDialog(this,
                            "La cantidad de días debe ser mayor que cero");
                    return null;
                }

                if (precioDia < 0) {
                    JOptionPane.showMessageDialog(this,
                            "El precio no puede ser negativo");
                    return null;
                }

                double subtotal = cantDias * precioDia;
                double impuesto = subtotal * 0.13;
                double total = subtotal + impuesto;

                reservaNueva.setCliente(cliente);
                reservaNueva.setVehiculo(vehiculo);
                reservaNueva.setCantDias(cantDias);
                reservaNueva.setPrecioDia(precioDia);
                reservaNueva.setImpuesto(impuesto);
                reservaNueva.setTotal(total);

                txtSubtotal.setText(String.valueOf(subtotal));
                txtImpuesto.setText(String.valueOf(impuesto));
                txtTotal.setText(String.valueOf(total));

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this,
                        "Los días y precio deben ser numéricos");
                return null;
            }

        } else {
            JOptionPane.showMessageDialog(this,
                    "Hay campos vacíos");
            return null;
        }

        return reservaNueva;
    }

    private void cargarDatos() {
        txtNumReserva.setText(String.valueOf(reserva.getNumReserva()));
        dtpFechaReserva.setDate(reserva.getFechaReserva());

        if (reserva.getCliente() != null) {
            cmbCliente.setSelectedIndex(
                    listaClientes.indexOf(reserva.getCliente()));
        }

        if (reserva.getVehiculo() != null) {
            cmbVehiculo.setSelectedIndex(
                    listaVehiculos.indexOf(reserva.getVehiculo()));
        }
        txtCantDias.setText(String.valueOf(reserva.getCantDias()));
        txtPrecioDia.setText(String.valueOf(reserva.getPrecioDia()));
        calcularTotales();
    }

